package com.joshua.r0th.jentikrumah.ui.home;

public class exp_holder {

    private String judul;
    private int img;

    public exp_holder(String judul, int img) {
        this.judul = judul;
        this.img = img;
    }

    public String getJudul() {
        return judul;
    }

    public void setJudul(String judul) {
        this.judul = judul;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
